package com.xuri.vo;

public class HySystem {
    private String id;
    private String userAggrement;
    private String userHelp;
    private int spe;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserAggrement() {
        return userAggrement;
    }

    public void setUserAggrement(String userAggrement) {
        this.userAggrement = userAggrement;
    }

    public int getSpe() {
        return spe;
    }

    public void setSpe(int spe) {
        this.spe = spe;
    }

    public String getUserHelp() {
        return userHelp;
    }

    public void setUserHelp(String userHelp) {
        this.userHelp = userHelp;
    }
}

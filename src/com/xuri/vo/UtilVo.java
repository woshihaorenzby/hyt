package com.xuri.vo;

public class UtilVo {
	private String id;
	private String col;
	private String table;
	private String tag;
	
	public String getTable() {
		return table;
	}
	public void setTable(String table) {
		this.table = table;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCol() {
		return col;
	}
	public void setCol(String col) {
		this.col = col;
	}

	
}
